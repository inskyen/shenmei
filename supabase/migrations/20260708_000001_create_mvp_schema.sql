for each row execute function public.set_updated_at();

-- 將既有 videos 舊資料轉成 legacy posts。
-- 這樣未來首頁從 videos 切到 posts 時，舊 20 條內容不會消失。
-- 舊資料目前沒有 auth user 可關聯，所以先使用 legacy_added_by 保留來源。
insert into public.posts (
  video_id,
  legacy_added_by,
  note,
  visibility,
  status,
  created_at,
  updated_at
)
select
  videos.id,
  coalesce(nullif(videos.added_by, ''), 'legacy'),
  case
    when char_length(trim(coalesce(videos.title, ''))) >= 10 then videos.title
    else '舊影片資料，推薦理由待補。'
  end,
  'public',
  'published',
  coalesce(videos.created_at, now()),
  now()
from public.videos
where not exists (
  select 1
  from public.posts
  where posts.video_id = videos.id
    and posts.legacy_added_by = coalesce(nullif(videos.added_by, ''), 'legacy')
);

