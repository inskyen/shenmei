-- 將舊欄位映射到新欄位。這一步不刪舊欄位，確保現有首頁仍能工作。
update public.videos
set
  source_platform = coalesce(source_platform, 'bilibili'),
  external_id = coalesce(external_id, bvid),
  source_url = coalesce(source_url, case when bvid is not null then 'https://www.bilibili.com/video/' || bvid else null end),
  cover_url = coalesce(cover_url, cover),
  author_name = coalesce(author_name, up_name),
  duration_seconds = coalesce(duration_seconds, duration),
  metadata = case
    when metadata = '{}'::jsonb then jsonb_build_object(
      'legacy_id', id,
      'up_mid', up_mid,
      'play_count', play_count,
      'fav_time', fav_time,
      'added_by', added_by,
      'legacy_status', status,
      'curator_id', curator_id
    )
    else metadata
  end;

