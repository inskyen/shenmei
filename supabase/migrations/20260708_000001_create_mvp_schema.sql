create or replace function public.update_post_like_count()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' and new.target_type = 'post' and new.reaction_type = 'like' then
    update public.posts
    set like_count = like_count + 1
    where id = new.target_id;
    return new;
  end if;

  if tg_op = 'DELETE' and old.target_type = 'post' and old.reaction_type = 'like' then
    update public.posts
    set like_count = greatest(like_count - 1, 0)
    where id = old.target_id;
    return old;
  end if;

  return null;
end;
$$;

drop trigger if exists reactions_update_post_like_count on public.reactions;
create trigger reactions_update_post_like_count
after insert or delete on public.reactions
for each row execute function public.update_post_like_count();

create or replace function public.update_post_comment_count()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' and new.target_type = 'post' and new.status = 'published' then
    update public.posts
    set comment_count = comment_count + 1
    where id = new.post_id;
    return new;
  end if;

  if tg_op = 'DELETE' and old.target_type = 'post' and old.status = 'published' then
    update public.posts
    set comment_count = greatest(comment_count - 1, 0)
    where id = old.post_id;
    return old;
  end if;

  return coalesce(new, old);
end;
$$;

