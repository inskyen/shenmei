-- 審美者 MVP schema（兼容既有 videos 表版本）
-- 目標：保留舊 videos 表的既有資料，再補上 video + post 雙核心所需的新表與欄位。
-- 注意：這份 SQL 先作為可審閱 migration，不會自動執行到遠端 Supabase。

create extension if not exists "pgcrypto";

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create or replace function public.generate_numeric_username()
returns text
language plpgsql
as $$
declare
  candidate text;
begin
  loop
    candidate := floor(10000000 + random() * 90000000)::bigint::text;
    exit when not exists (
      select 1
      from public.profiles
      where username = candidate
    );
  end loop;

  return candidate;
end;
$$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text not null unique,
  display_name text not null default '策展人',
  avatar_url text,
  bio text,
  aesthetic_tags text[] not null default '{}',
  message_permission text not null default 'followers',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  constraint profiles_username_format check (username ~ '^[a-zA-Z0-9_]{3,32}$'),
  constraint profiles_message_permission_check check (
    message_permission in ('everyone', 'followers', 'none')
  )
);

drop trigger if exists profiles_set_updated_at on public.profiles;
create trigger profiles_set_updated_at
before update on public.profiles
for each row execute function public.set_updated_at();

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, username, display_name)
  values (
    new.id,
    public.generate_numeric_username(),
    coalesce(
      nullif(new.raw_user_meta_data ->> 'display_name', ''),
      split_part(new.email, '@', 1),
      '策展人'
    )
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

-- 舊專案已存在 public.videos，且已有資料：
-- id / bvid / title / cover / up_name / up_mid / duration / play_count / fav_time / added_by / status / curator_id
-- 所以這裡只在表不存在時建立舊兼容結構；若表已存在，下面會用 alter table 補新欄位。
create table if not exists public.videos (
  id bigint generated by default as identity primary key,
  bvid text,
  title text not null,
  cover text,
  up_name text,
  up_mid bigint,
  duration integer,
  play_count integer,
  fav_time bigint,
  added_by text,
  status text,
  curator_id uuid,
  created_at timestamptz not null default now()
);

