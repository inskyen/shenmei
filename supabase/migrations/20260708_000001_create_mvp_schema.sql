create table if not exists public.posts (
  id uuid primary key default gen_random_uuid(),
  -- 舊 videos 資料目前只有 added_by 文字，未必能對應 auth.users。
  -- 因此 user_id 允許暫時為 null，legacy_added_by 用來保留舊資料來源。
  user_id uuid references public.profiles(id) on delete set null,
  legacy_added_by text,
  video_id bigint not null references public.videos(id) on delete cascade,
  note text not null,
  visibility text not null default 'public',
  status text not null default 'published',
  like_count integer not null default 0,
  comment_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  constraint posts_author_present check (user_id is not null or legacy_added_by is not null),
  constraint posts_note_min_length check (char_length(trim(note)) >= 10),
  constraint posts_visibility_check check (visibility in ('public', 'followers', 'private')),
  constraint posts_status_check check (status in ('draft', 'published', 'hidden', 'deleted')),
  constraint posts_like_count_non_negative check (like_count >= 0),
  constraint posts_comment_count_non_negative check (comment_count >= 0)
);

drop trigger if exists posts_set_updated_at on public.posts;
create trigger posts_set_updated_at
